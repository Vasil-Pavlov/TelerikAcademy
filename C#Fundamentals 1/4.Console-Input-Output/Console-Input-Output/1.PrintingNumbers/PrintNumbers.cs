using System;

class PrintNumbers
{
    static void Main()
    {
        int firstNumber = int.Parse(Console.ReadLine());
        int secondNumber = int.Parse(Console.ReadLine());
        int thirdNumber = int.Parse(Console.ReadLine());
        Console.Write("{0} {1} {2}",firstNumber,secondNumber,thirdNumber);
        Console.WriteLine();
    }
}
